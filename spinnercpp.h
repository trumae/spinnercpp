  class spinner {
  public:
  spinner() : delay_(200),
      chars_(0),
      finalmsg_(""),
      prefix_(""),
      suffix_(""),
      active_(true) {				
    }

    ~spinner() {
      stop();
    }

    void start() {
      active_ = true;
      
      cout << "\e[?25l";
      cout.flush();
      
      thr_ = thread([&](){
	  int c = 0;
	  auto chars = CharSets[chars_];
	  int l = CharSets[chars_].size();
	  
	  while (active_)  { 
	    cout << "\r" << chars[c++ % l];	    
	    cout.flush();
	    std::this_thread::sleep_for(delay_);
	  }
	});
    }
    
    void stop() {
      if (active_) {	
	active_ = false;
	thr_.join();
	cout << "\e[?25h";
	cout.flush();	
      }
    }

  private:
    chrono::duration<int,std::milli> delay_;
    string prefix_;
    string suffix_;
    int chars_;
    string finalmsg_;
    bool active_;
    mutex lock_;
    thread thr_;
  };
