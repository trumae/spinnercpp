	class spinner {
		public:
			spinner() : delay_(200),
			chars_(0),
			finalmsg_(""),
			prefix_(""),
			suffix_(""),
			active_(true) {				
			}

			~spinner() {
				stop();
			}

			void start() {
				thr_ = thread([&](){
						int c = 0;
						auto chars = CharSets[chars_];
						int l = CharSets[chars_].size();

						while (active_)  { 
						        cout << "\r" << chars[c++ % l]; 
							std::this_thread::sleep_for(delay_);
						}
				});
			}

			void stop() {
				active_ = false;
				std::this_thread::sleep_for(delay_);
			}

		private:
			chrono::duration<int,std::milli> delay_;
			string prefix_;
			string suffix_;
			int chars_;
			string finalmsg_;
			bool active_;
			mutex lock_;
			thread thr_;
	};
