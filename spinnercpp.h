	class spinner {
	  public:
		spinner(chrono::duration<size_t, std::milli> delay = 200ms,
				size_t charset = 0,
				string finalmsg = "",
				string prefix = "",
				string suffix = ""
		) : delay_(delay),
			chars_(CharSets[charset]),
			finalmsg_(std::move(finalmsg)),
			prefix_(std::move(prefix)),
			suffix_(std::move(suffix)),
			active_(false) {
		}

		~spinner() {
			stop();
		}

		virtual void start() {
			active_ = true;

			cout << "\e[?25l";
			cout.flush();

			thr_ = thread([&]() {
				size_t c = 0;
				size_t l = chars_.size();

				while (active_) {
					cout << "\r" << prefix_ << chars_[c++] << suffix_;
					cout.flush();
					c %= l;
					std::this_thread::sleep_for(delay_);
				}
			});
		}

		virtual void stop() {
			if (active_) {
				active_ = false;
				thr_.join();
				cout << "\e[?25h";
				if (!finalmsg_.empty())
					cout << " - " << finalmsg_ << endl; // endl flushes
			}
		}

	  private:
		const chrono::duration<size_t, std::milli> delay_;
		const vector<string>& chars_;
		const string finalmsg_;
		const string prefix_;
		const string suffix_;
		std::atomic_bool active_;
		// mutex lock_; Unused!
		thread thr_;
	};
